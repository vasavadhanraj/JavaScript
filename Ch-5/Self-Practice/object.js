//Object Literals
const person = {
    name: "Hari",
    age: 33,
    weight: "45kg"
}

console.log(person);
console.log(typeof person);
console.log(person.name);
console.log(person.age);
console.log(person.weight);

const example = {
    color: ["Orange","Green","Purple","Aqua"],
    number: "46",
    fruit: "Guava"
}

console.log(example["fruit"]);
console.log(example.fruit);
console.log(example.color);
console.log(example.color[3]);

